// JS Code read right to left and top to bottom
// Equal
var myName = "Dhanushka";
console.log(myName);

// Addition with equal
var age = 27;
age += 10; // age = age + 10
console.log(age);

// Substraction with equal
age -= 10; // age = age -10
console.log(age);

// Multiplication with equal
age *= 10; // age = age *10
console.log(age);

// Division with equal
age /= 4; // age = age /4
console.log(age);

// Modulus with equal
age %= 13; // age = age % 13
console.log(age);

// Exponentiation with equal
age **= 2; // age = age ** 2
console.log(age);